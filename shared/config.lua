Dadi = Dadi or {}

Dadi.aiming = false -- Allows the player to shoot without having to aim first.
Dadi.dispatchNPC = false -- Disables the NPC emergency services.
Dadi.restarVoice = false -- Resets the voice chat command. 
Dadi.voiceCommand = 'rvoz' -- Command to reset the voice chat.
Dadi.butt = false -- Disables the gun buttstock.
Dadi.idleCam = false -- Disables the idle camera.

-- // Configure the population density of your server // --
Dadi.removeNPC = false -- Removes the NPC Density.
Dadi.garbageTrucks = false -- Removes the garbage trucks.
Dadi.randomBoats = false -- Removes the random boats.
Dadi.densityMultiplier = 0.5 -- Configure the density of the population. 0 - 1 (0.5 default)
Dadi.vehicleDensity = 0.3 -- Configure the density of the vehicles. 0 - 1 (0.3 default)

Dadi.enemyNPC = false -- Disables violent NPCs.

-- // Configure duplicate plates in your server // --
Dadi.alwaysCheck = true -- If set to false it will only be checked when the vehicle is created, If set to true all vehicles are checked every 5 seconds.
Dadi.checkedTime = 5000 -- Time to check the plates. (5 seconds default)